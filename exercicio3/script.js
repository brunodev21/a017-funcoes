function soma (num1, num2) {
    return num1 + num2 
}

function subtracao (num1, num2) {
    return num1 - num2
}

function multiplicacao (num1, num2) {
    return num1 * num2 
}

function divisao (num1, num2) {
    return num1 / num2
}

let numero1 = (20)
let numero2 = (2)

console.log(`
A soma é: ${soma (numero1, numero2)} 
A subtração é: ${subtracao (numero1, numero2)}
A multiplicação é: ${multiplicacao (numero1, numero2)} 
A divisão é: ${divisao (numero1, numero2)} `)